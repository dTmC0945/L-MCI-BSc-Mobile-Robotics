class Vector_2D {   // Two-dimensional vector class.
public:             // has public access to data.
    double x;       // The x component of the vector.
    double y;       // The y component of the vector.
};

int main() {        // declare the main() function
    Vector_2D Obj;  // Crate an object Obj from Class
    Obj.x = 1.0;    // Set data member x to 1.0
    Obj.y = 2.0;    // Set data member y to 2.0
}